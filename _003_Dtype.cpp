#include <iostream>
using namespace std;

int main()
{
    // Data Types Primitive

    // Character data type
    char var1 = 'c'; // Declare and initialize a character variable
    cout << var1 << endl;

    // Boolean data type
    bool var2 = true; // Declare and initialize a boolean variable
    cout << var2 << endl;

    // Integer data types
    // Signed integer
    int var3 = INT16_MAX; // Maximum value of a 16-bit signed integer
    cout << var3 << endl;

    // Unsigned integer
    unsigned int var4 = UINT16_MAX; // Maximum value of a 16-bit unsigned integer
    cout << var4 << endl;

    // Floating-point data types
    // Single-precision floating-point number
    float pi = 3.14; // Declare and initialize a float variable
    cout << pi << endl;

    // Double-precision floating-point number
    double pi_double = 3.14159265358979323846; // Declare and initialize a double variable
    cout << pi_double << endl;

    return 0;
}
